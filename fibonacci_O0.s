	.file	"fibonacci.c"          ; Исходный файл на C
	.text                        ; Начало секции кода

; ============================================================
; ФУНКЦИЯ: fib
; Прототип C: uint64_t fib(int n)
; Описание: Вычисляет n-е число Фибоначчи
; Способ: итеративный алгоритм (цикл for)
; Уровень оптимизации: -O0 (без оптимизации)
; ============================================================
	.globl	fib                ; Делаем имя fib видимым для компоновщика
	.def	fib;	.scl	2;	.type	32;	.endef   ; Определение функции для Windows
	.seh_proc	fib            ; Начало structured exception handling пролога
fib:
	; ----- ПРОЛОГ ФУНКЦИИ (стандартный для -O0) -----
	pushq	%rbp               ; Сохраняем старый указатель базы стека
	.seh_pushreg	%rbp       ; (SEH директива: сохранили регистр rbp)
	movq	%rsp, %rbp         ; Устанавливаем новый указатель базы стека
	.seh_setframe	%rbp, 0    ; (SEH директива: фрейм установлен)
	subq	$32, %rsp          ; Выделяем 32 байта в стеке под локальные переменные
	.seh_stackalloc	32         ; (SEH директива: выделено 32 байта в стеке)
	.seh_endprologue           ; Конец пролога, начало тела функции

	; ----- СОХРАНЕНИЕ АРГУМЕНТА n -----
	movl	%ecx, 16(%rbp)     ; n (аргумент из ecx) сохраняется в стек по адресу [rbp+16]

	; ----- ПРОВЕРКА: n < 0 ? -----
	cmpl	$0, 16(%rbp)       ; Сравниваем n с 0
	jns	.L2                ; Если n >= 0 (знаковый бит не установлен), переход на .L2
	; Если n < 0:
	movl	$0, %eax           ; Код возврата = 0 (в регистре eax)
	jmp	.L3                ; Переход в конец функции (к метке .L3)

.L2:
	; ----- ИНИЦИАЛИЗАЦИЯ ПЕРЕМЕННЫХ a = 0, b = 1 -----
	movq	$0, -8(%rbp)       ; a = 0, сохраняем в стек по адресу [rbp-8]
	movq	$1, -16(%rbp)      ; b = 1, сохраняем в стек по адресу [rbp-16]

	; ----- ПРОВЕРКА: n == 0 ? -----
	cmpl	$0, 16(%rbp)       ; Сравниваем n с 0
	jne	.L4                ; Если n != 0, переход на .L4
	; Если n == 0:
	movq	-8(%rbp), %rax     ; Загружаем a (0) в регистр rax (возвращаемое значение)
	jmp	.L3                ; Переход в конец функции (к метке .L3)

.L4:
	; ----- ИНИЦИАЛИЗАЦИЯ ЦИКЛА: i = 2 -----
	movl	$2, -20(%rbp)      ; i = 2, сохраняем в стек по адресу [rbp-20]
	jmp	.L5                ; Переход к проверке условия цикла

.L6:
	; ============================================================
	; ТЕЛО ЦИКЛА for (i = 2; i <= n; i++)
	; ============================================================

	; --- ВЫЧИСЛЕНИЕ c = a + b ---
	movq	-8(%rbp), %rdx     ; Загружаем a из [rbp-8] в rdx
	movq	-16(%rbp), %rax    ; Загружаем b из [rbp-16] в rax
	addq	%rdx, %rax         ; rax = a + b
	movq	%rax, -32(%rbp)    ; c = a + b, сохраняем в стек по адресу [rbp-32]

	; --- ПРИСВАИВАНИЕ a = b ---
	movq	-16(%rbp), %rax    ; Загружаем b из [rbp-16] в rax
	movq	%rax, -8(%rbp)     ; Сохраняем rax в a по адресу [rbp-8]

	; --- ПРИСВАИВАНИЕ b = c ---
	movq	-32(%rbp), %rax    ; Загружаем c из [rbp-32] в rax
	movq	%rax, -16(%rbp)    ; Сохраняем rax в b по адресу [rbp-16]

	; --- ИНКРЕМЕНТ СЧЁТЧИКА i++ ---
	addl	$1, -20(%rbp)      ; Увеличиваем i на 1 в стеке по адресу [rbp-20]

.L5:
	; ============================================================
	; ПРОВЕРКА УСЛОВИЯ ЦИКЛА i <= n
	; ============================================================
	movl	-20(%rbp), %eax    ; Загружаем i из [rbp-20] в eax
	cmpl	16(%rbp), %eax     ; Сравниваем i с n (из [rbp+16])
	jle	.L6                ; Если i <= n, продолжаем цикл (переход на .L6)

	; ----- ВОЗВРАЩЕНИЕ РЕЗУЛЬТАТА b -----
	movq	-16(%rbp), %rax    ; Загружаем b из [rbp-16] в rax (возвращаемое значение)

.L3:
	; ----- ЭПИЛОГ ФУНКЦИИ -----
	addq	$32, %rsp          ; Освобождаем 32 байта стека
	popq	%rbp               ; Восстанавливаем старый rbp
	ret                        ; Возврат из функции
	.seh_endproc               ; Конец SEH процедуры

; ============================================================
; ВНЕШНИЕ ОБЪЯВЛЕНИЯ
; ============================================================
	.def	__main;	.scl	2;	.type	32;	.endef   ; Функция инициализации MinGW

; ============================================================
; СЕКЦИЯ ДАННЫХ ТОЛЬКО ДЛЯ ЧТЕНИЯ (.rdata)
; ============================================================
	.section .rdata,"dr"
.LC0:
	.ascii "Usage: %s <n>\12\0"   ; Строка для вывода справки (\12 = \n)
.LC1:
	.ascii "n \344\356\353\346\355\356 \341\373\362\374 >= 0\0"   ; "n должно быть >= 0" в UTF-8
.LC2:
	.ascii "fib(%d) = %I64u\12\0" ; Строка формата для вывода результата

; ============================================================
; ФУНКЦИЯ: main
; Прототип C: int main(int argc, char *argv[])
; Уровень оптимизации: -O0 (без оптимизации)
; ============================================================
	.text                        ; Возвращаемся в секцию кода
	.globl	main               ; Делаем имя main видимым для компоновщика
	.def	main;	.scl	2;	.type	32;	.endef   ; Определение функции для Windows
	.seh_proc	main           ; Начало SEH пролога
main:
	; ----- ПРОЛОГ ФУНКЦИИ MAIN -----
	pushq	%rbp               ; Сохраняем старый указатель базы стека
	.seh_pushreg	%rbp       ; (SEH директива: сохранили rbp)
	movq	%rsp, %rbp         ; Устанавливаем новый указатель базы стека
	.seh_setframe	%rbp, 0    ; (SEH директива: фрейм установлен)
	subq	$48, %rsp          ; Выделяем 48 байт в стеке под локальные переменные
	.seh_stackalloc	48         ; (SEH директива: выделено 48 байт)
	.seh_endprologue           ; Конец пролога, начало тела функции

	; ----- СОХРАНЕНИЕ АРГУМЕНТОВ argc и argv -----
	movl	%ecx, 16(%rbp)     ; argc (из ecx) сохраняется в стек по адресу [rbp+16]
	movq	%rdx, 24(%rbp)     ; argv (из rdx) сохраняется в стек по адресу [rbp+24]

	; ----- ИНИЦИАЛИЗАЦИЯ СРЕДЫ MinGW -----
	call	__main             ; Вызов функции инициализации MinGW

	; ----- ПРОВЕРКА: argc < 2 ? -----
	cmpl	$1, 16(%rbp)       ; Сравниваем argc с 1
	jg	.L8                ; Если argc > 1, переход на .L8 (есть аргумент)

	; --- Ветка: недостаточно аргументов ---
	; printf("Usage: %s <n>\n", argv[0]);
	movq	24(%rbp), %rax     ; Загружаем argv в rax
	movq	(%rax), %rax       ; Разыменовываем: rax = argv[0]
	movq	%rax, %rdx         ; rdx = argv[0] (второй аргумент printf)
	leaq	.LC0(%rip), %rcx   ; rcx = адрес строки .LC0 (первый аргумент printf)
	call	printf             ; Вызов printf
	movl	$1, %eax           ; Код возврата = 1
	jmp	.L9                ; Переход в конец main

.L8:
	; --- Ветка: есть аргумент командной строки ---
	; n = atoi(argv[1]);
	movq	24(%rbp), %rax     ; Загружаем argv в rax
	addq	$8, %rax           ; rax = argv + 8 (указатель на argv[1])
	movq	(%rax), %rax       ; Разыменовываем: rax = argv[1] (строка)
	movq	%rax, %rcx         ; rcx = argv[1] (первый аргумент atoi)
	call	atoi               ; Вызов atoi, результат в eax
	movl	%eax, -4(%rbp)     ; n = результат atoi, сохраняем в стек по адресу [rbp-4]

	; ----- ПРОВЕРКА: n < 0 ? -----
	cmpl	$0, -4(%rbp)       ; Сравниваем n с 0
	jns	.L10               ; Если n >= 0, переход на .L10

	; --- Ветка: n < 0 ---
	; puts("n должно быть >= 0");
	leaq	.LC1(%rip), %rcx   ; rcx = адрес строки .LC1 (первый аргумент puts)
	call	puts               ; Вызов puts
	movl	$1, %eax           ; Код возврата = 1
	jmp	.L9                ; Переход в конец main

.L10:
	; --- Ветка: n >= 0 ---
	; printf("fib(%d) = %llu\n", n, fib(n));
	movl	-4(%rbp), %eax     ; Загружаем n в eax
	movl	%eax, %ecx         ; rcx = n (первый аргумент fib)
	call	fib                ; Вызов fib(n), результат в rax
	movq	%rax, %rdx         ; rdx = результат fib(n) (будет третьим аргументом printf)
	movl	-4(%rbp), %eax     ; Снова загружаем n в eax
	movq	%rdx, %r8          ; r8 = результат fib(n) (третий аргумент printf)
	movl	%eax, %edx         ; edx = n (второй аргумент printf)
	leaq	.LC2(%rip), %rcx   ; rcx = адрес строки .LC2 (первый аргумент printf)
	call	printf             ; Вызов printf
	movl	$0, %eax           ; Код возврата = 0

.L9:
	; ----- ЭПИЛОГ ФУНКЦИИ MAIN -----
	addq	$48, %rsp          ; Освобождаем 48 байт стека
	popq	%rbp               ; Восстанавливаем старый rbp
	ret                        ; Возврат из функции
	.seh_endproc               ; Конец SEH процедуры

; ============================================================
; ИДЕНТИФИКАЦИЯ КОМПИЛЯТОРА И ВНЕШНИЕ ЗАВИСИМОСТИ
; ============================================================
	.ident	"GCC: (tdm64-1) 9.2.0"   ; Версия GCC
	.def	printf;	.scl	2;	.type	32;	.endef   ; Внешняя функция printf
	.def	atoi;	.scl	2;	.type	32;	.endef     ; Внешняя функция atoi
	.def	puts;	.scl	2;	.type	32;	.endef     ; Внешняя функция puts