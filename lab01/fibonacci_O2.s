	.file	"fibonacci.c"          ; Исходный файл на C
	.text                        ; Начало секции кода

; ============================================================
; ФУНКЦИЯ: fib
; Прототип C: uint64_t fib(int n)
; Уровень оптимизации: -O2 (оптимизация по скорости)
; Особенности: переменные в регистрах, цикл оптимизирован,
;              пролог и эпилог минимизированы
; ============================================================
	.p2align 4                 ; Выравнивание начала функции на 16 байт
	.globl	fib                ; Делаем имя fib видимым для компоновщика
	.def	fib;	.scl	2;	.type	32;	.endef   ; Определение функции для Windows
	.seh_proc	fib            ; Начало structured exception handling пролога
fib:
	.seh_endprologue           ; Конец пролога (он пустой — регистры не сохраняются)

	; ----- ИНИЦИАЛИЗАЦИЯ РЕЗУЛЬТАТА -----
	xorl	%eax, %eax         ; eax = 0 (обнуляем младшие 32 бита rax, результат по умолчанию)

	; ----- ПРОВЕРКА: n <= 0 ? -----
	testl	%ecx, %ecx         ; Проверяем ecx (аргумент n)
	jg	.L8                ; Если n > 0, переход на .L8
	; Если n <= 0:
	ret                        ; Возврат с результатом 0 (уже в rax)

	.p2align 4,,10             ; Выравнивание следующей метки
	.p2align 3
.L8:
	; ----- ПРОВЕРКА: n == 1 ? -----
	cmpl	$1, %ecx           ; Сравниваем n с 1
	je	.L5                ; Если n == 1, переход на .L5 (вернуть 1)

	; ----- ПОДГОТОВКА К ЦИКЛУ (n >= 2) -----
	addl	$1, %ecx           ; ecx = n + 1 (теперь это верхняя граница цикла + 1)
	movl	$2, %edx           ; edx = 2 (это счётчик i, начинаем с 2)
	movl	$1, %eax           ; eax = 1 (это переменная b, начинаем с 1)
	xorl	%r8d, %r8d         ; r8d = 0 (это переменная a, начинаем с 0)

	; Выравнивание тела цикла для производительности
	.p2align 4,,10
	.p2align 3
.L3:
	; ============================================================
	; ТЕЛО ЦИКЛА (оптимизированная версия)
	; Регистры: r8 = a, rax = b, r9 = временный, edx = i, ecx = n+1
	; ============================================================
	movq	%rax, %r9          ; r9 = старое значение b
	addl	$1, %edx           ; i++ (увеличиваем счётчик в edx)
	addq	%r8, %rax          ; rax = b + a (новое значение b, но пока в rax)
	movq	%r9, %r8           ; a = старое b (переносим сохранённое значение)

	; ----- ПРОВЕРКА УСЛОВИЯ: i != n+1 ? -----
	cmpl	%edx, %ecx         ; Сравниваем n+1 (ecx) с i (edx)
	jne	.L3                ; Если не равны, повторяем цикл

	; ----- ВОЗВРАТ РЕЗУЛЬТАТА -----
	ret                        ; Результат уже в rax (значение b)

	.p2align 4,,10
	.p2align 3
.L5:
	; ----- СЛУЧАЙ n == 1 -----
	movl	$1, %eax           ; rax = 1 (возвращаемое значение для n == 1)
	ret                        ; Возврат из функции
	.seh_endproc               ; Конец SEH процедуры

; ============================================================
; ВНЕШНИЕ ОБЪЯВЛЕНИЯ
; ============================================================
	.def	__main;	.scl	2;	.type	32;	.endef   ; Функция инициализации MinGW

; ============================================================
; СЕКЦИЯ ДАННЫХ ТОЛЬКО ДЛЯ ЧТЕНИЯ (.rdata)
; ============================================================
	.section .rdata,"dr"
.LC0:
	.ascii "Usage: %s <n>\12\0"   ; Строка для вывода справки (\12 = \n)
.LC1:
	.ascii "n \344\356\353\346\355\356 \341\373\362\374 >= 0\0"   ; "n должно быть >= 0" в UTF-8
.LC2:
	.ascii "fib(%d) = %I64u\12\0" ; Строка формата для вывода результата

; ============================================================
; ФУНКЦИЯ: main
; Прототип C: int main(int argc, char *argv[])
; Уровень оптимизации: -O2 (оптимизация по скорости)
; Особенности: функция fib ВСТРОЕНА (inlined) прямо в main,
;              используется минимальный набор регистров,
;              часть проверок скомбинирована для скорости
; ============================================================
	.section	.text.startup,"x"  ; Секция для кода, выполняемого при запуске
	.p2align 4                 ; Выравнивание начала функции
	.globl	main               ; Делаем имя main видимым для компоновщика
	.def	main;	.scl	2;	.type	32;	.endef   ; Определение функции для Windows
	.seh_proc	main           ; Начало SEH пролога
main:
	; ----- ПРОЛОГ ФУНКЦИИ MAIN (оптимизированный) -----
	pushq	%rsi               ; Сохраняем регистр rsi (будет использоваться для argv)
	.seh_pushreg	%rsi       ; (SEH директива: сохранили rsi)
	pushq	%rbx               ; Сохраняем регистр rbx (будет использоваться для argc)
	.seh_pushreg	%rbx       ; (SEH директива: сохранили rbx)
	subq	$40, %rsp          ; Выделяем 40 байт в стеке (под локальные нужды и вызовы)
	.seh_stackalloc	40         ; (SEH директива: выделено 40 байт)
	.seh_endprologue           ; Конец пролога

	; ----- СОХРАНЕНИЕ АРГУМЕНТОВ В РЕГИСТРАХ -----
	movl	%ecx, %ebx         ; ebx = argc (сохраняем в callee-saved регистр)
	movq	%rdx, %rsi         ; rsi = argv (сохраняем в callee-saved регистр)

	; ----- ИНИЦИАЛИЗАЦИЯ СРЕДЫ MinGW -----
	call	__main             ; Вызов функции инициализации MinGW

	; ----- ПРОВЕРКА: argc <= 1 ? -----
	cmpl	$1, %ebx           ; Сравниваем argc с 1
	jle	.L18               ; Если argc <= 1, переход на .L18 (вывод Usage)

	; --- Ветка: есть аргумент командной строки ---
	; n = atoi(argv[1]);
	movq	8(%rsi), %rcx      ; rcx = argv[1] (смещение 8 от argv даёт указатель на второй элемент)
	call	atoi               ; Вызов atoi, результат в eax

	; ----- ПРОВЕРКИ ЗНАЧЕНИЯ n (оптимизировано) -----
	movl	%eax, %edx         ; edx = n (сохраняем для последующего использования)
	testl	%eax, %eax         ; Проверяем знак n
	js	.L19               ; Если n < 0 (знаковый бит установлен), переход на .L19 (ошибка)
	je	.L15               ; Если n == 0, переход на .L15 (результат 0)
	cmpl	$1, %eax           ; Сравниваем n с 1
	je	.L16               ; Если n == 1, переход на .L16 (результат 1)

	; ----- ВСТРОЕННЫЙ ЦИКЛ ВЫЧИСЛЕНИЯ ФИБОНАЧЧИ (n >= 2) -----
	; Этот код — полная копия тела функции fib, встроенная прямо сюда
	; Регистры: r8 = b, rcx = a, r9 = временный, eax = i, r10d = n+1
	leal	1(%rax), %r10d     ; r10d = n + 1 (верхняя граница цикла, как в fib)
	movl	$1, %r8d           ; r8d = 1 (b, начальное значение)
	movl	$2, %eax           ; eax = 2 (i, счётчик цикла)
	xorl	%ecx, %ecx         ; ecx = 0 (a, начальное значение)

	; Выравнивание тела цикла для производительности
	.p2align 4,,10
	.p2align 3
.L14:
	; ============================================================
	; ВСТРОЕННОЕ ТЕЛО ЦИКЛА
	; ============================================================
	movq	%r8, %r9           ; r9 = старое значение b
	addl	$1, %eax           ; i++ (увеличиваем счётчик)
	addq	%rcx, %r8          ; r8 = a + b (новое значение b)
	movq	%r9, %rcx          ; a = старое b
	cmpl	%r10d, %eax        ; Сравниваем i с n+1
	jne	.L14               ; Если не равны, продолжаем цикл

.L13:
	; ----- ВЫВОД РЕЗУЛЬТАТА -----
	; printf("fib(%d) = %llu\n", n, r8);
	; edx всё ещё содержит n (сохранили в edx после atoi)
	; r8 содержит результат вычисления
	leaq	.LC2(%rip), %rcx   ; rcx = адрес строки формата .LC2
	call	printf             ; Вызов printf

	; ----- УСПЕШНОЕ ЗАВЕРШЕНИЕ (return 0) -----
	xorl	%eax, %eax         ; eax = 0 (код возврата)

.L9:
	; ----- ЭПИЛОГ ФУНКЦИИ MAIN -----
	addq	$40, %rsp          ; Освобождаем 40 байт стека
	popq	%rbx               ; Восстанавливаем rbx
	popq	%rsi               ; Восстанавливаем rsi
	ret                        ; Возврат из main

.L15:
	; --- Ветка: n == 0 ---
	xorl	%r8d, %r8d         ; r8d = 0 (результат для n == 0)
	jmp	.L13               ; Переход к выводу результата

.L18:
	; --- Ветка: недостаточно аргументов (argc <= 1) ---
	; printf("Usage: %s <n>\n", argv[0]);
	movq	(%rsi), %rdx       ; rdx = argv[0] (второй аргумент printf)
	leaq	.LC0(%rip), %rcx   ; rcx = адрес строки .LC0 (первый аргумент printf)
	call	printf             ; Вызов printf
	movl	$1, %eax           ; eax = 1 (код возврата)
	jmp	.L9                ; Переход к эпилогу

.L19:
	; --- Ветка: n < 0 ---
	; puts("n должно быть >= 0");
	leaq	.LC1(%rip), %rcx   ; rcx = адрес строки .LC1 (аргумент puts)
	call	puts               ; Вызов puts
	movl	$1, %eax           ; eax = 1 (код возврата)
	jmp	.L9                ; Переход к эпилогу

.L16:
	; --- Ветка: n == 1 ---
	movl	$1, %r8d           ; r8d = 1 (результат для n == 1)
	jmp	.L13               ; Переход к выводу результата
	.seh_endproc               ; Конец SEH процедуры

; ============================================================
; ИДЕНТИФИКАЦИЯ КОМПИЛЯТОРА И ВНЕШНИЕ ЗАВИСИМОСТИ
; ============================================================
	.ident	"GCC: (tdm64-1) 9.2.0"   ; Версия GCC
	.def	atoi;	.scl	2;	.type	32;	.endef     ; Внешняя функция atoi
	.def	printf;	.scl	2;	.type	32;	.endef   ; Внешняя функция printf
	.def	puts;	.scl	2;	.type	32;	.endef     ; Внешняя функция puts
